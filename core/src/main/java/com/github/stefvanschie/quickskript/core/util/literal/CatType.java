package com.github.stefvanschie.quickskript.core.util.literal;

/**
 * An enum for all cat types
 *
 * @since 0.1.0
 */
public enum CatType {

    /**
     * A black cat with orange eyes
     *
     * @since 0.1.0
     */
    ALL_BLACK,

    /**
     * A black and white cat with green eyes
     *
     * @since 0.1.0
     */
    BLACK,

    /**
     * A silver cat with yellow eyes
     *
     * @since 0.1.0
     */
    BRITISH_SHORTHAIR,

    /**
     * A orange, white, dark brown and yellow cat with blue eyes
     *
     * @since 0.1.0
     */
    CALICO,

    /**
     * A gray and white cat with gray and green eyes
     *
     * @since 0.1.0
     */
    JELLIE,

    /**
     * A cream cat with blue eyes
     *
     * @since 0.1.0
     */
    PERSIAN,

    /**
     * A white and amber cat with blue eyes
     *
     * @since 0.1.0
     */
    RAGDOLL,

    /**
     * An orange and white cat with green eyes
     *
     * @since 0.1.0
     */
    RED,

    /**
     * A white and brown cat with blue eyes
     *
     * @since 0.1.0
     */
    SIAMESE,

    /**
     * A brown and white cat with yellow eyes
     *
     * @since 0.1.0
     */
    TABBY,

    /**
     * A white and blue cat with yellow eyes
     *
     * @since 0.1.0
     */
    WHITE,
}
